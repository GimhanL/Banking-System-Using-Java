    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        // TODO add your handling code here:
             try{
            PreparedStatement stmt=conn.prepareStatement("select AccountNumber from Account");
           ResultSet rs=stmt.executeQuery();
           String UID = null;
           while (rs.next())
           {
               UID = rs.getString("AccountNumber").trim(); //u003
           }
           String IdValue = UID.substring(1);
           int ID =Integer.parseInt(IdValue);//3
        String IDS=null;   
        if(ID>0 && ID<9)
        {
            ID=ID+1;
            IDS = "A00"+ID;//U004
        
        }
        else if(ID>9 && ID<99)
        {
            ID =ID+1;
            IDS = "A0"+ID;
        }
        else if(ID>=99999)
        {
            ID=ID+1;
            IDS="A"+ID;
        }
       accnumber.setText(IDS);
        }
        catch (Exception e){
      JOptionPane.showMessageDialog(null,"error genarating id"+e);
        }
    }//GEN-LAST:event_formWindowOpened

