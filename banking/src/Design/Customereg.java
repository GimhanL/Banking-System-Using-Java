    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        boolean valid=true;
      
      if( fulname.getText().toString().isEmpty()||
         birth.getText().toString().isEmpty()||
         nationality.getText().toString().isEmpty()||
         accnumber.getText().toString().isEmpty()||
         mobnumber.getText().toString().isEmpty()||
         address.getText().toString().isEmpty()||
         nic.getText().toString().isEmpty() ||
         acctype.getSelectedItem().toString().isEmpty() ||
         gender.getSelectedItem().toString().isEmpty() )
        
        
           
     
          
      {
       JOptionPane.showMessageDialog(null,"Feild shoudn't be blank","Customer Register Form",JOptionPane.ERROR_MESSAGE);
       valid=false;
      }
      
      
      if(valid)
      {
          try{
           PreparedStatement statRegister=conn.prepareStatement("Insert into Account values(?,?,?,?,?,?,?,?,?,?,?)");
            statRegister.setString(1,accnumber.getText());
            statRegister.setString(2,fulname.getText());
            statRegister.setString(3,birth.getText());
              statRegister.setString(4,nationality.getText());
             statRegister.setString(5,gender.getSelectedItem().toString());
               statRegister.setString(6,acctype.getSelectedItem().toString());
               statRegister.setString(7,mobnumber.getText());
             statRegister.setString(8,address.getText());
              statRegister.setString(9,nic.getText());
                statRegister.setString(10,"500");
               statRegister.setString(11,"Deactive");
           
            int numRows=statRegister.executeUpdate();
            if(numRows>0){
                 JOptionPane.showMessageDialog(null,"New Customer Registered");  
            }  
            
              
            
        }
   
         catch (Exception exception)
            {
             JOptionPane.showMessageDialog(null,"Error encountered while entering data:  "+exception);
            }
      }
                                          

    
     
    }//GEN-LAST:event_jButton2ActionPerformed

